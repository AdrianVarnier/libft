#include "libft.h"

void	ft_lstclear(t_list **l, void (*del)(void *))
{
	t_list	*tmp;

	while (*l != 0)
	{
		tmp = (*l)->next;
		ft_lstdelone(*l, (*del));
		*l = tmp;
	}
	l = 0;
}
