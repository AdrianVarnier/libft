#include "libft.h"

int	ft_atoi(const char *s)
{
	int	x;
	int	y;
	int	signe;

	x = 0;
	y = 0;
	signe = 1;
	while (s[x] == ' ' || (s[x] >= 9 && s[x] <= 13))
		x++;
	if (s[x] == '-')
	{
		signe = -1;
		x++;
	}
	if (s[x] == '+')
		x++;
	while (ft_isdigit(s[x]) == 1)
	{
		y = 10 * y + s[x] - '0';
		x++;
	}
	return (y * signe);
}
